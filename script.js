// Helper Functions

function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log(add(6,4));
console.log(subtract(6,4));
console.log(multiply(6,4));
console.log(divide(6,4));